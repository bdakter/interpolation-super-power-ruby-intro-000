rainbow = ['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet']

def display_rainbow(color_array)
  p "R: #{color_array[0]},"
  p "O: #{color_array[1]},"
  p "Y: #{color_array[2]},"
  p "G: #{color_array[3]},"
  p "B: #{color_array[4]},"
  p "I: #{color_array[5]},"
  p "V: #{color_array[6]}"
end

display_rainbow(rainbow)
